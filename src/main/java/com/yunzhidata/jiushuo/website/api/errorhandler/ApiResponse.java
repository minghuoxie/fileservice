package com.yunzhidata.jiushuo.website.api.errorhandler;

public class ApiResponse {
    private int id;
    private String name;

    public ApiResponse(int id,String name){
        this.id=id;
        this.name=name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
